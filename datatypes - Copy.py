# Integer  - Whole Numbers
# Float    - Decimals
# Boolean  - True / False
# String   - Alphanumeric characters

a=10			# Implicit declaration
b=int(10)		# Explicit declaration

print(a)
print(type(a))

print(b)
print(type(b))

c=20.2
d=float(20.2)

print(c)
print(type(c))

print(d)
print(type(d))

e=True
f=bool(True)
g=bool(1)

print(e)
print(type(e))

print(f)
print(type(f))

print(g)
print(type(g))

h="Hello world"
i=str("Hello world")

print(h)
print(type(h))

print(i)
print(type(i))